// Function Declaration
function greet(name){
    return `Hello, ${name}`;
}
console.log(greet('Subarna'));

//Function Expression
const greeet = function(name){
    return `Hello, ${name}`;
};

console.log(greeet('Limbu'));

//Arrow Function
const great = (names)=> `Hello,${names}`;
console.log(great('Yes Sirrrrr'));

